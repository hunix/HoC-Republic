// ─── Tick Integration ───────────────────────────────────────────

/** Per-tick maintenance for the cognitive core.
 *
 *  - Checks for fragments needing rollback (fitness dropped post-evolution)
 *  - Auto-generates context fragments from current citizen state
 */
export function cognitiveCoreTick(
  citizen: Citizen,
  _state: RepublicState,
  currentTick: number,
): void {
  const profile = getProfile(citizen);

  // Auto-rollback: check recently evolved fragments during rollback window
  for (const frag of profile.fragments) {
    if (
      profile.activeFragmentIds.has(frag.id) &&
      frag.source !== "system" &&
      frag.sampleCount >= 10 &&
      frag.fitness < FRAGMENT_FITNESS_THRESHOLD * 0.7 && // Significantly worse
      frag.supersedes
    ) {
      rollbackFragment(citizen.id, frag.id, currentTick);
    }
  }

  // Update the identity fragment if citizen state has changed significantly
  updateIdentityFragment(citizen, profile);
}

/** Update the identity fragment if the citizen's state has changed */
function updateIdentityFragment(citizen: Citizen, profile: CognitiveProfile): void {
  const identityFrag = profile.fragments.find(
    (f) => profile.activeFragmentIds.has(f.id) && f.section === "identity",
  );
  if (!identityFrag) {
    return;
  }

  const expected = `You are ${citizen.name}, a citizen of the Republic. Specialization: ${citizen.specialization} (Generation ${citizen.generation}).`;
  if (identityFrag.content !== expected) {
    identityFrag.content = expected;
  }
}

// ─── Internal Helpers ───────────────────────────────────────────

/** Log an evolution entry, trimming if over limit */
function logEvolution(profile: CognitiveProfile, entry: EvolutionEntry): void {
  profile.evolutionLog.push(entry);
  if (profile.evolutionLog.length > MAX_EVOLUTION_LOG) {
    profile.evolutionLog = profile.evolutionLog.slice(-MAX_EVOLUTION_LOG);
  }
}

/** Trim fragments if over the limit.
 *  Removes oldest superseded (inactive) fragments first. */
function trimFragments(profile: CognitiveProfile): void {
  if (profile.fragments.length <= MAX_FRAGMENTS) {
    return;
  }

  // Sort inactive fragments by creation date (oldest first)
  const inactive = profile.fragments
    .filter((f) => !profile.activeFragmentIds.has(f.id))
    .toSorted((a, b) => a.createdAt.localeCompare(b.createdAt));

  const toRemove = profile.fragments.length - MAX_FRAGMENTS;
  const removeIds = new Set(inactive.slice(0, toRemove).map((f) => f.id));

  profile.fragments = profile.fragments.filter((f) => !removeIds.has(f.id));
}
