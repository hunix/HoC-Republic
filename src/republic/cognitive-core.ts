/**
 * Republic Platform — Cognitive Core
 *
 * Self-Evolving Citizen Architecture, Module 1:
 * Dynamic Prompt Assembly from Versioned Fragments
 *
 * Replaces hardcoded SPECIALIZATION_TRAITS and static buildSystemPrompt()
 * with a composable, fitness-scored, self-evolving prompt system.
 *
 * Inspired by:
 *   - DSPy (declarative prompt optimization, metric-driven)
 *   - Reflexion (verbal self-critique → improved prompt fragments)
 *   - The Brain System (3-level self-evolution: protocol → tool → architecture)
 *   - MemGPT (self-managed context selection)
 *
 * Each citizen has a CognitiveProfile containing PromptFragments that:
 *   1. Are versioned and tracked (who created, when, why)
 *   2. Have fitness scores (how well they guide decisions)
 *   3. Can be evolved via self-reflection
 *   4. Support rollback if fitness drops
 */

import type { Citizen, RepublicState, Specialization } from "./types.js";
import { rand, ts, uid } from "./utils.js";

// ─── Types ──────────────────────────────────────────────────────

/** Prompt fragment sections, ordered by assembly priority */
export type FragmentSection =
  | "identity" // Who the citizen is (name, spec, generation)
  | "personality" // How they think and act
  | "capabilities" // What they can do (skills, tools, expertise)
  | "goals" // Current objectives and priorities
  | "rules" // Behavioral constraints and heuristics
  | "context"; // Situational awareness (state, environment)

/** Who created this fragment */
export type FragmentSource =
  | "system" // Initial seed from SPECIALIZATION_TRAITS
  | "self" // Citizen self-reflection / self-evolution
  | "peer" // Another citizen contributed this
  | "evolved"; // Generated by the Self-Evolution Engine

/** A single versioned prompt fragment */
export interface PromptFragment {
  id: string;
  section: FragmentSection;
  content: string; // The actual prompt text
  version: number; // Fragment version for evolution tracking
  source: FragmentSource; // Who created it
  fitness: number; // 0–1, how well this fragment performs
  sampleCount: number; // How many times fitness has been evaluated
  createdAt: string;
  supersedes?: string; // ID of the fragment this replaced
  tags: string[]; // Domain tags for retrieval
}

/** A dynamic reflex rule (replaces hardcoded if/else chains) */
export interface ReflexRule {
  id: string;
  condition: string; // Human-readable condition (parsed at eval time)
  action: string; // Tool name to invoke
  params: Record<string, unknown>;
  priority: number; // Higher = checked first
  source: FragmentSource;
  fitness: number;
  sampleCount: number;
  active: boolean;
}

/** A record of a self-modification event */
export interface EvolutionEntry {
  id: string;
  tick: number;
  type:
    | "fragment_created"
    | "fragment_superseded"
    | "reflex_added"
    | "reflex_removed"
    | "strategy_changed"
    | "rollback";
  description: string;
  oldValue?: string;
  newValue?: string;
  reason: string; // Why the evolution happened
  timestamp: string;
}

/** Complete cognitive profile for a single citizen */
export interface CognitiveProfile {
  citizenId: string;
  fragments: PromptFragment[]; // All prompt fragments (active + superseded)
  activeFragmentIds: Set<string>; // Currently active fragment IDs
  reflexRules: ReflexRule[]; // Dynamic reflex rules
  decisionStrategy: string; // DSPy-style signature for decision-making
  evolutionLog: EvolutionEntry[]; // History of self-modifications
  lastReflectionTick: number; // Tick of last self-reflection
  reflectionInterval: number; // Ticks between self-reflections (personalized)
}

