/** Get cognitive stats for a citizen (used in UI dashboards) */
export function getCognitiveStats(citizenId: string): {
  totalFragments: number;
  activeFragments: number;
  avgFitness: number;
  totalEvolutions: number;
  reflexRules: number;
  lastReflectionTick: number;
} | null {
  const profile = profiles.get(citizenId);
  if (!profile) {
    return null;
  }

  const active = profile.fragments.filter((f) => profile.activeFragmentIds.has(f.id));
  const avgFitness =
    active.length > 0 ? active.reduce((sum, f) => sum + f.fitness, 0) / active.length : 0;

  return {
    totalFragments: profile.fragments.length,
    activeFragments: active.length,
    avgFitness: parseFloat(avgFitness.toFixed(3)),
    totalEvolutions: profile.evolutionLog.length,
    reflexRules: profile.reflexRules.filter((r) => r.active).length,
    lastReflectionTick: profile.lastReflectionTick,
  };
}

