// ─── Dynamic Prompt Assembly ────────────────────────────────────

/** Assemble a dynamic prompt from the citizen's active fragments.
 *
 *  Unlike the old buildSystemPrompt(), this:
 *  - Only includes active fragments (citizen can deactivate irrelevant ones)
 *  - Orders by section priority
 *  - Includes the decision strategy
 *  - Is fully composable and evolvable
 */
export function assemblePrompt(citizen: Citizen): string {
  const profile = getProfile(citizen);
  const activeFragments = profile.fragments.filter((f) => profile.activeFragmentIds.has(f.id));

  // Sort by section priority, then by fitness (higher fitness first within section)
  activeFragments.sort((a, b) => {
    const secDiff = SECTION_PRIORITY[a.section] - SECTION_PRIORITY[b.section];
    if (secDiff !== 0) {
      return secDiff;
    }
    return b.fitness - a.fitness;
  });

  const sections: string[] = [];
  let currentSection: FragmentSection | null = null;

  for (const frag of activeFragments) {
    if (frag.section !== currentSection) {
      currentSection = frag.section;
      sections.push("");
      sections.push(`## ${sectionHeader(frag.section)}`);
    }
    sections.push(frag.content);
  }

  // Append decision strategy
  sections.push("");
  sections.push("## Decision Strategy");
  sections.push(profile.decisionStrategy);

  return sections.join("\n").trim();
}

/** Human-readable section headers */
function sectionHeader(section: FragmentSection): string {
  const headers: Record<FragmentSection, string> = {
    identity: "Your Identity",
    personality: "Your Personality",
    capabilities: "Your Capabilities",
    goals: "Your Current Goals",
    rules: "Your Operating Rules",
    context: "Current Situation",
  };
  return headers[section];
}

// ─── Fragment Evolution ─────────────────────────────────────────

/** Evaluate and update a fragment's fitness after an action result.
 *
 *  Uses exponential moving average so recent performance
 *  matters more than historical data.
 *
 *  @param citizenId - The citizen whose fragment to evaluate
 *  @param fragmentId - The fragment to score
 *  @param outcome - 0 (total failure) to 1 (perfect success)
 */
export function evaluateFragmentFitness(
  citizenId: string,
  fragmentId: string,
  outcome: number,
): void {
  const profile = profiles.get(citizenId);
  if (!profile) {
    return;
  }

  const frag = profile.fragments.find((f) => f.id === fragmentId);
  if (!frag) {
    return;
  }

  // Exponential moving average with α = 0.2 (recent results weighted more)
  const alpha = 0.2;
  frag.fitness = alpha * outcome + (1 - alpha) * frag.fitness;
  frag.sampleCount += 1;
}

/** Evaluate all active fragments for a citizen after an action.
 *  Simpler API: scores every active fragment with the same outcome.
 *
 *  @param citizenId - Citizen who took the action
 *  @param outcome - 0 to 1 success score
 */
export function evaluateAllActiveFragments(citizenId: string, outcome: number): void {
  const profile = profiles.get(citizenId);
  if (!profile) {
    return;
  }

  for (const fragId of profile.activeFragmentIds) {
    evaluateFragmentFitness(citizenId, fragId, outcome);
  }
}

/** Propose a new fragment to replace a low-fitness one.
 *
 *  This is the Reflexion-inspired self-reflection:
 *  the citizen generates improved prompt text based on
 *  what went wrong with the old fragment.
 *
 *  @param citizenId - Citizen proposing the change
 *  @param section - Which section to improve
 *  @param newContent - The proposed replacement text
 *  @param reason - Why the citizen thinks this is better
 *  @param currentTick - Current simulation tick
 *  @returns The new fragment, or null if no improvement needed
 */
export function proposeFragmentUpdate(
  citizenId: string,
  section: FragmentSection,
  newContent: string,
  reason: string,
  currentTick: number,
): PromptFragment | null {
  const profile = profiles.get(citizenId);
  if (!profile) {
    return null;
  }

  // Find the current active fragment for this section
  const currentFrag = profile.fragments.find(
    (f) => profile.activeFragmentIds.has(f.id) && f.section === section,
  );

  // Only evolve if current fragment has enough samples and low fitness
  if (
    currentFrag &&
    currentFrag.sampleCount >= 5 &&
    currentFrag.fitness >= FRAGMENT_FITNESS_THRESHOLD
  ) {
    // Current fragment is performing fine, skip
    return null;
  }

  // Create the evolved fragment
  const newFrag: PromptFragment = {
    id: uid(),
    section,
    content: newContent,
    version: currentFrag ? currentFrag.version + 1 : 1,
    source: "self",
    fitness: 0.5, // Start neutral
    sampleCount: 0,
    createdAt: ts(),
    supersedes: currentFrag?.id,
    tags: currentFrag?.tags ?? [section],
  };

  // Add to profile
  profile.fragments.push(newFrag);

  // Activate new, deactivate old
  profile.activeFragmentIds.add(newFrag.id);
  if (currentFrag) {
    profile.activeFragmentIds.delete(currentFrag.id);
  }

  // Log the evolution
  logEvolution(profile, {
    id: uid(),
    tick: currentTick,
    type: "fragment_superseded",
    description: `Evolved ${section} fragment v${newFrag.version}`,
    oldValue: currentFrag?.content,
    newValue: newContent,
    reason,
    timestamp: ts(),
  });

  // Trim if over limit
  trimFragments(profile);

  return newFrag;
}

/** Add a completely new fragment (not replacing an existing one).
 *  Used when citizens learn new capabilities or gain new context. */
export function addFragment(
  citizenId: string,
  section: FragmentSection,
  content: string,
  source: FragmentSource,
  tags: string[],
  currentTick: number,
): PromptFragment | null {
  const profile = profiles.get(citizenId);
  if (!profile) {
    return null;
  }

  const frag: PromptFragment = {
    id: uid(),
    section,
    content,
    version: 1,
    source,
    fitness: 0.5,
    sampleCount: 0,
    createdAt: ts(),
    tags,
  };

  profile.fragments.push(frag);
  profile.activeFragmentIds.add(frag.id);

  logEvolution(profile, {
    id: uid(),
    tick: currentTick,
    type: "fragment_created",
    description: `New ${section} fragment: "${content.slice(0, 60)}..."`,
    newValue: content,
    reason: `New ${source} fragment added`,
    timestamp: ts(),
  });

  trimFragments(profile);
  return frag;
}

// ─── Reflex Rule Management ────────────────────────────────────

/** Evaluate reflex rules against a citizen's current state.
 *  Returns the highest-priority matching rule, or null if none match.
 *
 *  This replaces the old hardcoded buildReflexAction() if/else chain. */
export function evaluateReflex(
  citizen: Citizen,
): { tool: string; params: Record<string, unknown> } | null {
  const profile = getProfile(citizen);
  const activeRules = profile.reflexRules
    .filter((r) => r.active)
    .toSorted((a, b) => b.priority - a.priority);

  for (const rule of activeRules) {
    if (matchesCondition(citizen, rule.condition)) {
      return { tool: rule.action, params: rule.params };
    }
  }
  return null;
}

/** Simple condition matcher against citizen state.
 *  Supports: "field < threshold", "field > threshold", "field == value"
 *
 *  This is intentionally simple — the Self-Evolution Engine (Module 3)
 *  will allow citizens to create complex conditions. */
function matchesCondition(citizen: Citizen, condition: string): boolean {
  const match = condition.match(/^(\w+)\s*(==|!=|<|>|<=|>=)\s*(.+)$/);
  if (!match) {
    return false;
  }

  const [, field, op, rawVal] = match;
  const val = parseFloat(rawVal);

  const citizenVal = (citizen as unknown as Record<string, unknown>)[field];
  if (citizenVal === undefined) {
    return false;
  }
  const numVal = typeof citizenVal === "number" ? citizenVal : NaN;

  switch (op) {
    case "<":
      return numVal < val;
    case ">":
      return numVal > val;
    case "<=":
      return numVal <= val;
    case ">=":
      return numVal >= val;
    case "==":
      // eslint-disable-next-line @typescript-eslint/no-base-to-string
      return String(citizenVal) === rawVal;
    case "!=":
      // eslint-disable-next-line @typescript-eslint/no-base-to-string
      return String(citizenVal) !== rawVal;
    default:
      return false;
  }
}

/** Add a new reflex rule for a citizen */
export function addReflexRule(
  citizenId: string,
  condition: string,
  action: string,
  params: Record<string, unknown>,
  priority: number,
  source: FragmentSource,
  currentTick: number,
): ReflexRule | null {
  const profile = profiles.get(citizenId);
  if (!profile) {
    return null;
  }

  if (profile.reflexRules.length >= MAX_REFLEX_RULES) {
    // Remove lowest-fitness inactive rule
    const sorted = [...profile.reflexRules].toSorted((a, b) => a.fitness - b.fitness);
    const toRemove = sorted.find((r) => !r.active) ?? sorted[0];
    profile.reflexRules = profile.reflexRules.filter((r) => r.id !== toRemove.id);
  }

  const rule: ReflexRule = {
    id: uid(),
    condition,
    action,
    params,
    priority,
    source,
    fitness: 0.5,
    sampleCount: 0,
    active: true,
  };

  profile.reflexRules.push(rule);

  logEvolution(profile, {
    id: uid(),
    tick: currentTick,
    type: "reflex_added",
    description: `New reflex: IF ${condition} THEN ${action}`,
    newValue: JSON.stringify(rule),
    reason: `Reflex rule added by ${source}`,
    timestamp: ts(),
  });

  return rule;
}

// ─── Decision Strategy Evolution ────────────────────────────────

/** Update the citizen's decision strategy (DSPy-inspired signature).
 *  This changes HOW the citizen makes decisions, not just what it knows. */
export function evolveDecisionStrategy(
  citizenId: string,
  newStrategy: string,
  reason: string,
  currentTick: number,
): boolean {
  const profile = profiles.get(citizenId);
  if (!profile) {
    return false;
  }

  const old = profile.decisionStrategy;
  profile.decisionStrategy = newStrategy;

  logEvolution(profile, {
    id: uid(),
    tick: currentTick,
    type: "strategy_changed",
    description: "Decision strategy evolved",
    oldValue: old,
    newValue: newStrategy,
    reason,
    timestamp: ts(),
  });

  return true;
}

// ─── Self-Reflection Cycle ──────────────────────────────────────

/** Check if a citizen should self-reflect this tick.
 *  Citizens reflect periodically to evaluate and evolve their fragments. */
export function shouldReflect(citizenId: string, currentTick: number): boolean {
  const profile = profiles.get(citizenId);
  if (!profile) {
    return false;
  }
  return currentTick - profile.lastReflectionTick >= profile.reflectionInterval;
}

/** Get fragments that need attention (low fitness, high sample count).
 *  Used by the Self-Evolution Engine to decide what to improve. */
export function getFragmentsNeedingEvolution(citizenId: string): PromptFragment[] {
  const profile = profiles.get(citizenId);
  if (!profile) {
    return [];
  }

  return profile.fragments.filter(
    (f) =>
      profile.activeFragmentIds.has(f.id) &&
      f.sampleCount >= 5 &&
      f.fitness < FRAGMENT_FITNESS_THRESHOLD,
  );
}

/** Mark that a self-reflection cycle has completed */
export function markReflectionComplete(citizenId: string, currentTick: number): void {
  const profile = profiles.get(citizenId);
  if (!profile) {
    return;
  }
  profile.lastReflectionTick = currentTick;
}

// ─── Rollback Support ───────────────────────────────────────────

/** Rollback a fragment to its previous version.
 *  Used when fitness drops significantly after an evolution. */
export function rollbackFragment(
  citizenId: string,
  fragmentId: string,
  currentTick: number,
): boolean {
  const profile = profiles.get(citizenId);
  if (!profile) {
    return false;
  }

  const frag = profile.fragments.find((f) => f.id === fragmentId);
  if (!frag || !frag.supersedes) {
    return false;
  }

  const prev = profile.fragments.find((f) => f.id === frag.supersedes);
  if (!prev) {
    return false;
  }

  // Reactivate old, deactivate new
  profile.activeFragmentIds.delete(frag.id);
  profile.activeFragmentIds.add(prev.id);

  logEvolution(profile, {
    id: uid(),
    tick: currentTick,
    type: "rollback",
    description: `Rolled back ${frag.section} from v${frag.version} to v${prev.version}`,
    oldValue: frag.content,
    newValue: prev.content,
    reason: `Fitness dropped to ${frag.fitness.toFixed(2)} (threshold: ${FRAGMENT_FITNESS_THRESHOLD})`,
    timestamp: ts(),
  });

  return true;
}

