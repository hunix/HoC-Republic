// ─── State Sync ─────────────────────────────────────────────────

/** Serialize profiles for state persistence.
 *  Called by the tick loop when saving state. */
export function serializeCognitiveState(): Record<
  string,
  {
    fragments: PromptFragment[];
    activeFragmentIds: string[];
    reflexRules: ReflexRule[];
    decisionStrategy: string;
    evolutionLog: EvolutionEntry[];
    lastReflectionTick: number;
    reflectionInterval: number;
  }
> {
  // Using 'Record<string, unknown>' for intermediate accumulation; cast satisfies the declared return type
  const out = {} as ReturnType<typeof serializeCognitiveState>;
  for (const [cid, profile] of profiles) {
    out[cid] = {
      fragments: profile.fragments,
      activeFragmentIds: [...profile.activeFragmentIds],
      reflexRules: profile.reflexRules,
      decisionStrategy: profile.decisionStrategy,
      evolutionLog: profile.evolutionLog,
      lastReflectionTick: profile.lastReflectionTick,
      reflectionInterval: profile.reflectionInterval,
    };
  }
  return out;
}

/** Restore profiles from persisted state.
 *  Called at startup when loading a snapshot. */
export function restoreCognitiveState(
  data: Record<
    string,
    {
      fragments: PromptFragment[];
      activeFragmentIds: string[];
      reflexRules: ReflexRule[];
      decisionStrategy: string;
      evolutionLog: EvolutionEntry[];
      lastReflectionTick: number;
      reflectionInterval: number;
    }
  >,
): void {
  profiles.clear();
  for (const [cid, pData] of Object.entries(data)) {
    profiles.set(cid, {
      citizenId: cid,
      fragments: pData.fragments,
      activeFragmentIds: new Set(pData.activeFragmentIds),
      reflexRules: pData.reflexRules,
      decisionStrategy: pData.decisionStrategy,
      evolutionLog: pData.evolutionLog,
      lastReflectionTick: pData.lastReflectionTick,
      reflectionInterval: pData.reflectionInterval,
    });
  }
}

// ─── Profile Management ─────────────────────────────────────────

/** Get or create a cognitive profile for a citizen.
 *  On first access, seeds the profile with specialization-based fragments. */
export function getProfile(citizen: Citizen): CognitiveProfile {
  let profile = profiles.get(citizen.id);
  if (!profile) {
    profile = createSeedProfile(citizen);
    profiles.set(citizen.id, profile);
  }
  return profile;
}

